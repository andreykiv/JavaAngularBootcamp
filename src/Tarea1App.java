
public class Tarea1App {

	public static void main(String[] args) {
		double myNum = 5;
		double myNum2 = 9;
		
		double suma = myNum + myNum2;
		double resta = myNum - myNum2;
		double multip = myNum*myNum2;
		double divis = myNum/myNum2;
		double modul = myNum%myNum2;
		
		System.out.println("Suma: " + suma);
		System.out.println("Resta: " + resta);
		System.out.println("Multiplicación: " + multip);
		System.out.println("División: " + divis);
		System.out.println("Módulo: " + modul);
	}

}
